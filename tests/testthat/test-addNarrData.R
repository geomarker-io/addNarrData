test_that("addNarrData adds temp and humidity", {
  d_narr_cell <- get_narr_cell_numbers(d_input())

  d <- get_narr_data(d_narr_cell,
                     narr_variables = c('air.2m', 'rhum.2m'))
  expect_equal(
    d$air.2m,
    d_output()$air.2m,
    tolerance = 0.0004
  )
  expect_equal(
    d$rhum.2m,
    d_output()$rhum.2m,
    tolerance = 0.0004
  )
})
